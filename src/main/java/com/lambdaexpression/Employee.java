package com.lambdaexpression;

public class Employee {
    private int ID;
    private int salary;
    private int name;

    public int getID() {
        return ID;
    }

    public int getSalary() {
        return salary;
    }

    public int getName() {
        return name;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }

    public void setName(int name) {
        this.name = name;
    }

    public void setID(int ID) {
        this.ID = ID;
    }
}
